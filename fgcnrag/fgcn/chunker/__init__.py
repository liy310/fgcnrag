"""
文本分块包
==========

本包提供文本分块（Chunking）功能，将长文本切分为适合检索的小块。

为什么需要分块？
1. 向量模型有最大输入限制
2. 小块检索更精准，语义更集中
3. 便于控制上下文长度和生成质量

分块策略：
┌─────────────────────────────────────────────────────────────────┐
│                        原始长文本                                │
│    第一回 甄士隐梦幻曲通灵 ... 第二回 贾雨村演说荣国府 ...       │
└─────────────────────────────────────────────────────────────────┘
                                ↓
                    ┌───────────────────────────┐
                    │      章节识别 (正则)        │
                    │  "第X回"、"第X章" 模式匹配   │
                    └───────────────────────────┘
                                ↓
┌──────────────────┐  ┌──────────────────┐  ┌──────────────────┐
│     第一回       │  │     第二回       │  │     第三回       │
│   章节内容...    │  │   章节内容...    │  │   章节内容...    │
└──────────────────┘  └──────────────────┘  └──────────────────┘
                                ↓
                    ┌───────────────────────────┐
                    │   递归字符分块 (LangChain)  │
                    │  chunk_size=500, overlap=100│
                    └───────────────────────────┘
                                ↓
┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐ ┌──────────┐
│  块1     │ │  块2     │ │  块3     │ │  块4     │ │  块5     │
│ (500字)  │ │ (100字重叠│ │ (500字)  │ │ (100字重叠│ │ (500字)  │
└──────────┘ └──────────┘ └──────────┘ └──────────┘ └──────────┘
"""
