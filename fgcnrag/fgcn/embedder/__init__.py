"""
向量嵌入包
==========

本包提供文本向量化的核心功能。

什么是向量嵌入？
将文本转换为数值向量（数字列表），使得语义相近的文本在向量空间中距离较近。

向量化流程：
┌─────────────────────────────────────────────────────────────────┐
│                         "孙悟空的性格特点"                        │
│                            (中文文本)                             │
└─────────────────────────────────────────────────────────────────┘
                                ↓
                    ┌───────────────────────────┐
                    │   阿里百炼text-embedding   │
                    │         -v4模型            │
                    └───────────────────────────┘
                                ↓
┌─────────────────────────────────────────────────────────────────┐
│  [0.023, -0.087, 0.152, 0.031, -0.198, ..., 0.089]               │
│                      (1024维稠密向量)                            │
└─────────────────────────────────────────────────────────────────┘

为什么用阿里百炼？
1. 专门针对中文优化
2. 1024维向量，丰富的语义表达
3. 阿里云服务，稳定可靠

核心函数：
- generate_dense_vector(text): 单文本向量化
- generate_dense_vectors(texts): 批量向量化

使用示例：
```python
from fgcnrag.fgcn.embedder import generate_dense_vector

# 单条向量化
vector = generate_dense_vector("贾宝玉和林黛玉的关系")

# 批量向量化
vectors = generate_dense_vectors(["文本1", "文本2", "文本3"])
```
"""
