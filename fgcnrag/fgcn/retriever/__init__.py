"""
检索器包
========

本包提供文档检索功能。

什么是检索器？
在RAG系统中，检索器负责从向量数据库中找到与用户问题最相关的文档。

检索器类型：
┌─────────────────────────────────────────────────────────────────┐
│                        HybridRetriever                          │
│                         (混合检索器)                             │
├─────────────────────────────────────────────────────────────────┤
│                                                                 │
│   ┌─────────────────────┐      ┌─────────────────────┐         │
│   │   向量检索           │      │   关键词检索         │         │
│   │   (语义相似度)       │      │   (文本匹配)         │         │
│   ├─────────────────────┤      ├─────────────────────┤         │
│   │ 优点：理解语义       │      │ 优点：精确匹配       │         │
│   │ 缺点：专有名词不准   │      │ 缺点：无法理解语义   │         │
│   └─────────────────────┘      └─────────────────────┘         │
│                   │                      │                      │
│                   └──────────┬───────────┘                      │
│                              ↓                                   │
│                    ┌─────────────────────┐                      │
│                    │   结果合并去重        │                      │
│                    │   返回Top-K结果      │                      │
│                    └─────────────────────┘                      │
└─────────────────────────────────────────────────────────────────┘

使用示例：
```python
from fgcnrag.fgcn.database import init_database
from fgcnrag.fgcn.retriever import HybridRetriever

db = init_database()
retriever = HybridRetriever(db, top_k=5)

# 检索相关文档
docs = retriever.get_relevant_documents("林黛玉的特点")
```
"""
